/**
 * Generates email-safe PNG assets (emails can't rely on SVG).
 * Run: node tools/email-assets.mjs   → public/email/*.png
 */
import { createCanvas, loadImage, Path2D } from "@napi-rs/canvas";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const ROOT = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const OUT = path.join(ROOT, "public", "email");
fs.mkdirSync(OUT, { recursive: true });

const GREEN = "#3D5A3E";

function save(name, canvas) {
  fs.writeFileSync(path.join(OUT, name), canvas.toBuffer("image/png"));
  console.log("✓ public/email/" + name, `(${canvas.width}×${canvas.height})`);
}

// ── White wordmark for the dark email header (2x for retina) ──────────────────
{
  const logo = await loadImage(path.join(ROOT, "public", "logo.svg"));
  const H = 120; const W = Math.round(H * (logo.width / logo.height));
  const c = createCanvas(W, H);
  const ctx = c.getContext("2d");
  ctx.drawImage(logo, 0, 0, W, H);
  // Tint every opaque pixel white, preserving the alpha (the logo shape).
  ctx.globalCompositeOperation = "source-in";
  ctx.fillStyle = "#FFFFFF";
  ctx.fillRect(0, 0, W, H);
  save("logo-white.png", c);
}

