  const c = createCanvas(S, S);
  const ctx = c.getContext("2d");
  ctx.fillStyle = GREEN;
  ctx.beginPath();
  ctx.arc(S / 2, S / 2, S / 2, 0, Math.PI * 2);
  ctx.fill();
  // White glyph centred (24px path → ~34px)
