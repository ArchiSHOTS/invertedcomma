// Derive unique categories from the quotes
export const QUOTE_CATEGORIES = Array.from(
  new Set(SEED_QUOTES.map((q) => q.category))
).sort();

// Deduplicate and extract all unique tags with counts
export const AVAILABLE_TAGS = Array.from(
  new Set(SEED_QUOTES.flatMap((q) => q.tags))
).sort();

// Enrich quotes with additional cross-category tags and default sourceType
export const getEnrichedQuotes = (): Quote[] => {
  return SEED_QUOTES.map((q) => {
    const extraTags: string[] = [];
    if (q.tags.includes("design")) extraTags.push("innovation", "creation");
    if (q.tags.includes("philosophy")) extraTags.push("existentialism", "rationality");
    if (q.tags.includes("architecture")) extraTags.push("space", "structure");
    if (q.tags.includes("motivation")) extraTags.push("discipline", "grit");
    if (q.tags.includes("business")) extraTags.push("strategy");
    if (q.tags.includes("science")) extraTags.push("rationality");
    if (q.tags.includes("stoicism")) extraTags.push("resilience");

    return {
      ...q,
      sourceType: q.sourceType ?? "book",
      tags: Array.from(new Set([...q.tags, ...extraTags])),
    };
  });
};

// Helper: get quote by slug
export const getQuoteBySlug = (slug: string): Quote | undefined => {
  return SEED_QUOTES.find((q) => q.slug === slug);
};
